import { readFileSync, writeFileSync } from "node:fs";
import { dirname, relative, resolve } from "node:path";
import process from "node:process";
import { fileURLToPath, pathToFileURL } from "node:url";
import { CONTRACT_METADATA } from "../contract-metadata.js";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, "..");

export const DEFAULT_MODEL_SOURCE_PATH = resolve(
  scriptDir,
  "model-catalog-source.json",
);
export const DEFAULT_MODEL_TARGET_PATH = resolve(
  repoRoot,
  "src",
  "constants",
  "model-catalog.ts",
);

export function readModelCatalogSource(sourcePath = DEFAULT_MODEL_SOURCE_PATH) {
  const resolvedSourcePath = resolve(sourcePath);
  const source = JSON.parse(readFileSync(resolvedSourcePath, "utf8"));

  if (!Array.isArray(source.models)) {
    throw new Error(
      `Expected ${resolvedSourcePath} to contain a "models" array.`,
    );
  }

  if (
    source.modelCatalogVersion !==
    CONTRACT_METADATA.verifiedCodex.modelCatalogVersion
  ) {
    throw new Error(
      `${resolvedSourcePath} declares modelCatalogVersion "${source.modelCatalogVersion}", expected "${CONTRACT_METADATA.verifiedCodex.modelCatalogVersion}".`,
    );
  }

  return source;
}

export function selectSupportedModels(models) {
  const supportedModelIds = new Set(
    CONTRACT_METADATA.supportedModels.map((model) => model.modelId),
  );
  const selected = models.filter((model) => supportedModelIds.has(model.slug));

  if (selected.length !== supportedModelIds.size) {
    const selectedIds = new Set(selected.map((model) => model.slug));
    const missingModelIds = [...supportedModelIds].filter(
      (modelId) => !selectedIds.has(modelId),
    );
    throw new Error(
      `Model catalog source is missing supported model metadata for: ${missingModelIds.join(", ")}`,
    );
  }

  return selected;
}

export function renderModelCatalogModule({
  models,
  sourcePath = DEFAULT_MODEL_SOURCE_PATH,
} = {}) {
  if (!Array.isArray(models)) {
    throw new Error("Expected renderModelCatalogModule() to receive models.");
  }

  const resolvedSourcePath = resolve(sourcePath);
  const sourceLabel =
    relative(repoRoot, resolvedSourcePath) || resolvedSourcePath;
  const selected = selectSupportedModels(models);

  return [
    "// This file is generated by `npm run model-catalog:generate`.",
    `// Source snapshot: ${sourceLabel}`,
    "// Do not edit by hand.",
    "",
    'import { VERIFIED_CODEX_MODEL_CATALOG_VERSION } from "./gateway.js";',
    "",
    "type ModelCatalogPrimitive = boolean | number | string | null;",
    "",
    "type ModelCatalogValue =",
    "  | ModelCatalogPrimitive",
    "  | { readonly [key: string]: ModelCatalogValue }",
    "  | readonly ModelCatalogValue[];",
    "",
    "interface ModelCatalogEntryShape {",
    "  readonly slug: string;",
    "  readonly display_name: string;",
    "  readonly description?: string;",
    "  readonly priority: number;",
    "  readonly visibility: string;",
    "  readonly supported_in_api: boolean;",
    "  readonly [key: string]: ModelCatalogValue | undefined;",
    "}",
    "",
    "interface ModelCatalogShape {",
    "  readonly models: readonly ModelCatalogEntryShape[];",
    "}",
    "",
    "export const GONKAGATE_MODEL_CATALOG_VERSION =",
    "  VERIFIED_CODEX_MODEL_CATALOG_VERSION;",
    "",
    `export const GONKAGATE_MODEL_CATALOG = ${JSON.stringify(
      { models: selected },
      null,
      2,
    )} as const satisfies ModelCatalogShape;`,
    "",
    "export type ModelCatalogEntry =",
    "  (typeof GONKAGATE_MODEL_CATALOG.models)[number];",
    "",
    "export interface ModelCatalog {",
    "  readonly models: readonly ModelCatalogEntry[];",
    "}",
    "",
  ].join("\n");
}

export function writeModelCatalogModule({
  sourcePath = DEFAULT_MODEL_SOURCE_PATH,
  targetPath = DEFAULT_MODEL_TARGET_PATH,
} = {}) {
  const resolvedSourcePath = resolve(sourcePath);
  const resolvedTargetPath = resolve(targetPath);
  const source = readModelCatalogSource(resolvedSourcePath);
  const fileContents = renderModelCatalogModule({
    models: source.models,
    sourcePath: resolvedSourcePath,
  });

  writeFileSync(resolvedTargetPath, fileContents, "utf8");
  return fileContents;
}

function main(argv = process.argv.slice(2)) {
  const [
    sourcePath = DEFAULT_MODEL_SOURCE_PATH,
    targetPath = DEFAULT_MODEL_TARGET_PATH,
  ] = argv;

  writeModelCatalogModule({
    sourcePath,
    targetPath,
  });
}

const isEntrypoint =
  process.argv[1] !== undefined &&
  import.meta.url === pathToFileURL(process.argv[1]).href;

if (isEntrypoint) {
  main();
}
